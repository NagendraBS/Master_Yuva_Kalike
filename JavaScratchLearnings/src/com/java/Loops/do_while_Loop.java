 package com.java.Loops;

public class do_while_Loop {

	public static void main(String[] args) {

		int i = 0;
		
		do {
			
			System.out.println("I Will Execute First as Statement and I Will not Wait for the Condition");
			System.out.print(i + " " );
			i++ ;
			
		}
		
		while(i <= 5);

		
	}

}
