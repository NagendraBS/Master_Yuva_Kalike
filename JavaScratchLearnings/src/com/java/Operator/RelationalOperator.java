package com.java.Operator;

public class RelationalOperator {

	public static void main(String[] args) {

	        // Comparison operators
	        int a = 10;
	        int b = 3;
	        int c = 5;
	        int d = 3;

	        System.out.println("a > b: " + (a > b));
	        System.out.println("a < b: " + (a < b));
	        System.out.println("a >= b: " + (a >= b));
	        System.out.println("a <= b: " + (a <= b));
	        System.out.println("a == c: " + (a == c));
	        System.out.println("a != c: " + (a != c));
	        System.out.println("d == c: " + (d == c));

	    
		
	}

}
