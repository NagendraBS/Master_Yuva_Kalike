package demoTest;

public class Test3 {

	// Switch Case Statements
	public static void main(String[] args) {
		
	 int value = 100;

	 switch (value) {
	 
	 case 300 : System.out.println("Number is 300");
	 break;
	 
	 case 200 : System.out.println("Number is 200");
	 break;
	 
	 case 400 : System.out.println("Number is 400");
	 break;
	 
	 case 100 : System.out.println("Number is 100");
	 break;
	 
	 default : 
		 System.out.println("Value is Not Number");
	 
	 }
	 	
	}
}
